    private void jList1MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseReleased
        String title = (String) jList1.getSelectedValue();
        
        for(ConferencePaperDetails conf : checkedoutConfPaperList){
            if(conf.cTitle == title){
               selectedConf = conf;
               rid = conf.rid;
                jTextField1.setText(conf.cCheckoutDate);
                jTextField2.setText(conf.cDueDate);
                prevDueDate = conf.cDueDate;
                break;
            }
        }

    }//GEN-LAST:event_jList1MouseReleased


    private void populateCheckedoutConfList() {
        DefaultListModel dlm = new DefaultListModel();
        
        checkedoutConfPaperList = DatabaseHelper.getCheckedOutConfPapers();
        for(int i=0; i< checkedoutConfPaperList.size(); i++)
        {
            ConferencePaperDetails confPaper = checkedoutConfPaperList.get(i);
            dlm.addElement(confPaper.cTitle);
            title = confPaper.cTitle;
          
        }
        
        System.out.println("in func populate checkout list");
        jList1.setModel(dlm);
    }

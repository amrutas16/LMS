    private void roomCheckoutButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_roomCheckoutButtonActionPerformed

        

// get room no and update checkout table
        
        if ( jListRequestRoom.isSelectionEmpty())
              JOptionPane.showMessageDialog(null, "Please Select Room to checkout");
        
        
         String roomNo = (String) jListRequestRoom.getSelectedValue();

        for(RoomDetails room : roomList){
            if(room.roomNo == roomNo){
                try {
                    rid = room.rid;
                    
                    DatabaseHelper.checkoutRoom(rid,PatronDetails.pid);
                     JOptionPane.showMessageDialog(null, "checkout Succcess");
                    
                    break;
                } catch (SQLException ex) {
                    Logger.getLogger(PatronDetailsFrame.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(null, "FailToCheckout"+ex.getMessage());
                }
            }
        }
        
        
        
        
        
    }//GEN-LAST:event_roomCheckoutButtonActionPerformed


       private void populateBillList() throws SQLException {
        
        DefaultListModel dlm = new DefaultListModel();
        
        billList =  DatabaseHelper.getBill();
        //dlm.addElement("Resource    :    Resource ID     :    Amount ");
        for(int i=0; i< billList.size(); i++)
        {
            BillDetails bill = billList.get(i);
            dlm.addElement(bill.resourceType+"     :    "+bill.rid+"     :    "+bill.amount);
            jListAllBill.setModel(dlm);
          
        }
       }
        
       
    

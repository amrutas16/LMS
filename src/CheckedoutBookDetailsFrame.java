    private void jList1MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseReleased
        String title = (String) jList1.getSelectedValue();

        for(BookDetails book : checkedOutBookList){
            if(book.bTitle == title){
               selectedBook = book;
               rid = selectedBook.rid;
                jTextField1.setText(book.bCheckoutDate);
                jTextField2.setText(book.bDueDate);
                prevDueDate = book.bDueDate;
                break;
            }
        }

    }//GEN-LAST:event_jList1MouseReleased


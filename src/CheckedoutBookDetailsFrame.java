    private void populateCheckoutList() {
        DefaultListModel dlm = new DefaultListModel();
        
        checkedOutBookList = DatabaseHelper.getCheckedOutBooks();
        for(int i=0; i< checkedOutBookList.size(); i++)
        {
            BookDetails book = checkedOutBookList.get(i);
            dlm.addElement(book.bTitle);
            title = book.bTitle;
            //System.out.println(book.bTitle);
        }
        jList1.setModel(dlm);
        System.out.println("in func populate checkout list");
    }
